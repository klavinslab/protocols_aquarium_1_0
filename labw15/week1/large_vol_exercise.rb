#information "This protocol is for practicing pipetting large volumes."


step
  description:"Label Caps"
  note:
  "Take out two 1.5 mL tubes, close the caps, and label them A and B on the top of each cap."
  warning: "Be careful to always use a sterile technique when handling the tubes."
  image: "Labeled_tubes_large_volume_exercise"
end

step
  description: "Add Solution I to Tube A"
  note:
  "Look up to the 400 level of the bay and take the test tube rack with four tubes of colored water (red, green, blue, purple). Add 100 µL of Solution I to the tube labeled A, making sure to open and close the caps of the tube and test
  tube by using a sterile technique. Be sure to use the correct pipette and set it to 100 µL before adding a tip."
  image: "Add_solution_I_to_A_large_volume_exercise"
end

step
  description: "Add Solution I to Tube B"
  note:
   "Using a fresh tip and maintaining a sterile technique, add 150 µL of Solution I to Tube B. Be sure to change
   the volume of the pipette before adding a new tip."
  image: "Add_solution_I_to_B_large_volume_exercise"
end

